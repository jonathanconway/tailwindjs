/**
 * {value}rem
 *
 * Returns a numeric value in rem units.
 *
 * @param value
 *
 * @returns "{value}rem"
 */
export function rem(value: number) {
  return `${value}rem`;
}

/**
 * {value}px
 *
 * Returns a numeric value in pixel units.
 *
 * @param value
 *
 * @returns "{value}px"
 */
export function px(value: number) {
  return `${value}px`;
}
