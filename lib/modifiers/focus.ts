

/**
 * focus
 * 
 * Style an element when it has focus using the focus modifier:
 *
 * CSS:
 * - `&:focus`
 *
 * @param suffix Utility to modify
 * 
 * @see https://tailwindcss.com/docs/hover-focus-and-other-states#focus
 *
 * @type modifier
 */
export const focus = (suffix: string) => `focus:${suffix}`;
 
